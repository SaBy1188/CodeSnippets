package de.basics.kontrollstrukturen;

public class forLoop {
public static void main(String[] args) {

    // Zählerschleife
    // int i = 0 -> Startpunkt (Intialisierung)
    // i < 10 -> Bedingung, so lange bis..
    // i++ -> Zählvariable (i-- oder i+= ..)
    for(int i = 2; i < 10; i++) {
        System.out.println(i);
    }
}
}